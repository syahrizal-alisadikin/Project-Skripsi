     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $mahasiswa = $request->fk_mahasiswa_id;
        foreach($mahasiswa as $item){
            Jadwal::create([
                'fk_kelas_id' => $request->fk_kelas_id,
                'fk_mahasiswa_id' => $item,
            ]);
        }

        return redirect()->route('kelas.show',$request->fk_kelas_id)->with('success','Mahasiswa berhasil di tambahkan!!');
    }

